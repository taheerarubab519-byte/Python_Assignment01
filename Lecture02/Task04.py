number1 = int(input("Enter first number: "))
number2 = int(input("Enter second number: "))
number3 = int(input("Enter third number: "))
sum_numbers = number1 + number2 + number3
product_numbers = number1 * number2 * number3
average_numbers = sum_numbers / 3

print(f"The sum of the three numbers is {sum_numbers}.")
print(f"The product of the three numbers is {product_numbers}.")
print(f"The average of the three numbers is {average_numbers}.")